<?php

return [
    /*
    |--------------------------------------------------------------------------
    | Доверенные IP-адреса (белый список)
    |--------------------------------------------------------------------------
    |
    | Запросы с этих IP не подвергаются rate limiting.
    | Используется для внутренних сервисов Docker-сети (Telegram бот, воркеры).
    | Формат: список IP или CIDR через запятую (например: 172.18.0.0/16,10.0.0.5).
    |
    */
    'trusted_ips' => array_filter(
        array_map('trim', explode(',', env('RATE_LIMIT_TRUSTED_IPS', '')))
    ),
];
